            <p class="texto-justificado">
            El programa esta basado en la búsqueda de la reducción de la contaminación, que azota a nuestro estado y que facilita la organización de los contenedores de las clasificaciones de la basura orgánica e inorgánica
                <br>
                <br>
                <p><b>El planteamiento de BETSSO</b></p>
                Es un sistema con la capacidad de realizar un análisis sobre la basura que se encuentra dentro de un contenedor, así identifica todo el contenido orgánico e inorgánico como los objetos extraños que se ubican dentro de un contenedor.
                <br>
                <br>
                <p><b>La problematica de BETSSO</b></p>
                Se identificaron las diversas problematicas sobre la basura, nosotros logramos identificar una de ellas que era la ubicacion de la basura en su lugar correspondiente, la organizacion de la basura juega un papel bastante relevante, facilita la reutilizacion y agiliza los procesos del deposito del la basura.
                <br>
                <br>
                
                <p><b>Zonas de implementacion</b></p>


                
<ul>
<li type="disc">implementación por parte del Ayuntamiento. “El uso de nuestro contenedor inteligente podría verse impulsado en mas distritos”.</li>

<li type="disc">Implementación en parques “Las zonas más propensas a tener basura en una zona urbana”.</li>

<li type="disc">Implementación en Fraccionamientos “Las zonas residenciales con mayor unión y más accesibles”.</li>

<li type="disc">implementación en Avenidas “El lugar más transitado por la población”.</li>

</ul>

La implementacion puede realizarse en cualquier parte, por ahora decidimos centrarlo por distritos, entendiendo a los distritos como ubicaciones centradas por zonas en especifico.









